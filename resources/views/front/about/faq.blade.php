                        <dd>تشمل الخدمة كل السوريين الذين يحملون تأشيرة زيارة بغض النظر عن تاريخ دخولهم إلى المملكة كما تشمل الخدمة كل اليمنيين الذين لم تصدر لهم إقامة قبل تاريخ ١٤٣٦/٦/٢٠ هـ بحيث تصحح وزارة العمل والتنمية الاجتماعية أوضاعهم بالنسب والشروط المحددة.</dd>

                        <dt><i class="fa fa-envelope-o"></i>  ما هي طريقة الحصول على الأوراق الثبوتية لليمنيين من السفارة؟</dt>
                        <dd>خصص مندوب من السفارة اليمنية في فروع الجوازات بالمناطق إضافة إلى الرقم ٩٩٢ والبريد الالكتروني:
                            <span dir="ltr">992@gdp.gov.sa</span></dd>

                        <dt><i class="fa fa-envelope-o"></i> كيف ينهي الزائر العقد المبرم مع المنشأة أو الفرد؟</dt>
