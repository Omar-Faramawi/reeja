            </div>
            <div class="row">
                <div class="col-xs-12 col-md-10 col-md-offset-1">
                    <dl>
                        <dt><i class="fa fa-envelope-o"></i> ما هو إشعار "الزائرين؟</dt>
                        <dd>إشعار "الزائرين"  هو وثيقة قانونية تسمح للزائرين بالعمل لدى الجهة أو الفرد المستفيد من خدماتهم دون الحاجة إلى نقل الخدمات، حسب المدة المحددة في الإشعار.</dd>

                        <dt><i class="fa fa-envelope-o"></i> هل الخدمة محصورة على فئة معينة من الزائرين؟</dt>
                        <dd>حالياً الخدمة موجهة للزائرين اليمنيين والسوريين فقط.</dd>

                        <dt><i class="fa fa-envelope-o"></i> هل يلزم حصول الزائر على موافقة رسمية من أي جهة للعمل؟</dt>
                        <dd>لا، حيث يغني الإشعار المصدر عنها.</dd>

                        <dt><i class="fa fa-envelope-o"></i> ما هي البيانات المطلوبة لإصدار الإشعار؟</dt>
                        <dd>تحتاج إلى الرقم الحدودي وتاريخ ميلاد الزائر لاستخراج الإشعار.</dd>

                        <dt><i class="fa fa-envelope-o"></i> كم يكلف إشعار الزائرين؟</dt>
                        <dd>إصدار إشعار الزائرين مجاني.</dd>

                        <dt><i class="fa fa-envelope-o"></i> ما هي صلاحية إشعار الزائرين؟</dt>
                        <dd>إشعار الزائرين صالح لمدة ٦ أشهر قابلة للتمديد بناء على الامر السامي.</dd>

                        <dt><i class="fa fa-envelope-o"></i> ما هي المنشآت التي يحق لها الاستفادة من الخدمة؟</dt>
                        <dd>جميع المنشآت في النطاق الأخضر المنخفض فما فوق بعدد التأشيرات المسموح للمنشأة.</dd>

                        <dt><i class="fa fa-envelope-o"></i> هل يؤثر إصدار إشعار زائر على نسبة السعودة لدى المنشآت؟</dt>
                        <dd>نعم.</dd>

                        <dt><i class="fa fa-envelope-o"></i> هل يحق للأفراد الاستفادة من الخدمة؟</dt>
                        <dd>نعم، يحق للأفراد الإستفادة من الخدمة فيما يتعلق <u>بالجالية اليمنية فقط</u> بحيث يكون الحد الأعلى لعدد الإشعارات المصدرة خمسة إشعارات للزائرين . </dd>

                        <dt><i class="fa fa-envelope-o"></i> ما هي النشاطات المسموحة في الخدمة وما هي أحجامها؟</dt>
                        <dd>جميع النشاطات وجميع الأحجام المعتمدة من قبل وزارة العمل والتنمية الاجتماعية .</dd>

                        <dt><i class="fa fa-envelope-o"></i> ما هو الحد الأقصى للاستفادة من خدمة الزائرين للمنشآت ؟</dt>
                        <dd>حسب التأشيرات المتاحة للمنشأة المحددة . </dd>

                        <dt><i class="fa fa-envelope-o"></i> هل يلزم توقيع عقد بين المستفيد (المنشأة أو الفرد) والزائر؟</dt>
                        <dd>نعم.</dd>

                        <dt><i class="fa fa-envelope-o"></i> ما هي الحقوق الضابطة للمنشأة والزائر؟</dt>
                        <dd>يحدد هذه الحقوق للطرفين العقد المبرم بين الزائر والمنشأة أو الفرد.</dd>

                        <dt><i class="fa fa-envelope-o"></i> هل يمكن تغيير المهنة من خلال الخدمة؟</dt>
                        <dd>لا يمكن ذلك.</dd>

                        <dt><i class="fa fa-envelope-o"></i> هل يسمح للإناث الإستفادة من الخدمة  وإصدار إشعارات عمل مؤقتة؟</dt>
                        <dd>يسمح للإناث من الجالية السورية فقط العمل وإصدار إشعارات عمل مؤقتة</dd>

                        <dt><i class="fa fa-envelope-o"></i> ما هي شروط الزائرين المشمولين بالخدمة؟</dt>
                        <dd>الجالية اليمنية يشترط أن يكون العمر : 18 سنة فما فوق  الجنس : ذكر وأن يحمل الزائر بطاقة زائر<br>
                            الجالية السورية يشترط أن يكون العمر من 18 – 60  وأن يحمل الزائر تأشيرة زيارة.
                        </dd>

                        <dt><i class="fa fa-envelope-o"></i>  ما هي الحقوق الضابطة للمنشأة والزائر؟</dt>
                        <dd>يحدد هذه الحقوق للطرفين العقد المبرم بين الزائر والمنشأة أو الفرد .</dd>

                        <dt><i class="fa fa-envelope-o"></i> ما هي الشروط الضابطة للعمال الزائرين (الحالة الصحية، السوابق، المؤهلات)؟</dt>
                        <dd>الشروط الموضحة في نظام العمل.</dd>

                        <dt><i class="fa fa-envelope-o"></i> هل يسمح للهارب من كفيله بالعمل عن طريق خدمة الزائرين؟</dt>
                        <dd>لا.</dd>

                        <dt><i class="fa fa-envelope-o"></i> هل تشمل الخدمة السوريين المقيمين في المملكة بطريقة غير نظامية ؟</dt>
