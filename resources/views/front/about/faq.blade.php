                        <dt><i class="fa fa-envelope-o"></i> هل يسمح أجير لخدمات المرافقين للمرافقين المتواجدين خارج المملكة بالعمل؟</dt>
                        <dd>لا يسمح لهم بالعمل حيث أن هذه الخدمة متاحة فقط للمرافقين المتواجدين حاليا في المملكة.</dd>
                        <dt><i class="fa fa-envelope-o"></i> هل سيتم التعامل مع المرافقين الذين تم التعاقد معهم عن طريق أجير بنظام العمل أو نظام الجوازات؟</dt>
                        <dd>سيتم التعامل معهم بنظام العمل حيث أن وزارة العمل والتنمية الاجتماعية صرحت لهم بالعمل المؤقت .</dd>
                        <dt><i class="fa fa-envelope-o"></i> هل يمكنني كمرافق أرغب أن أعمل في قطاعات التعليم أن أسجل معلوماتي لدى أجير ليتم ابلاغ المنشآت التي لديها شواغر وظيفية؟</dt>
                        <dd>لا يمكن ذلك حاليا، وسيتم ابلاغكم عن طريق هذه الصفحة في حال إضافة هذه الخدمة.</dd>
                        <dt><i class="fa fa-envelope-o"></i> هل سيتم احتساب المرافقين الذين تم التعاقد معهم في برنامج نطاقات؟</dt>
                        <dd>نعم، سيتم احتسابهم لمدة عام واحد في نطاقات ويكون لهم تأثير في نسبة التوطين وحجم المنشأة، ويجدر بالتنويه أنه في حال تم فسخ عقد العمل، وترك المرافق صاحب العمل، فإنه لن يتم الغاء احتسابه من نطاقات.</dd>
                        <dt><i class="fa fa-envelope-o"></i> هل هناك أي ضوابط يجب استيفائها ليتم اصدار اشعار عمل مؤقت للمرافقين للعمل في المنشآت التعليمية؟</dt>
                        <dd>نعم، يجب استيفاء نفس شروط نظام العمل التي تنطبق في حال نقل الخدمات والتي تشمل شروط نطاقات في وقت تقديم الطلب. ويشمل ذلك لا للحصر: أن يكون نطاق المنشأة أخضر أو بلاتيني، أن يكون عمر المرافق قد تجاوز الـ 18عام، أن يكون قد دخل للمملكة بطريقة نظامية ومتواجد فيها وأن يكون حاملا لإقامة سارية.</dd>
                        <dt><i class="fa fa-envelope-o"></i> في حال تعاقد مرافق مع منشأة مستخدما أجير، هل سيتم نقل خدمات المرافق الى أجير أو المنشأة؟</dt>
                        <dd>لا يتم نقل خدمات المرافق في هذه الحال، ويتم الاكتفاء بإشعار أجير فقط .</dd>
                        <dt><i class="fa fa-envelope-o"></i> كجهة متعاقدة مع المرافقين، هل يجب تسجيلهم في التأمينات الاجتماعية؟</dt>
                        <dd>نعم يجب ذلك حيث أن نفس قوانين نظام العمل الخاصة بنقل الخدمات تُطبق في أجير.</dd>
                        <dt><i class="fa fa-envelope-o"></i> هل يجب على المرافقين الحصول على أي موافقة مقررة قبل بدء العمل؟</dt>
                        <dd>نعم، وينبغي على المستفيد من خدمات المرافق الحصول على الموافقات اللازمة من جهات ذات العلاقة إن وجدت.</dd>
                        <dt><i class="fa fa-envelope-o"></i> ماذا لو تم فسخ عقد العمل بين المرافق والجهة الموظفة لأي سبب من الاسباب، هل سيتم الغاء تأشيرة المرافق وإصدار خروج نهائي له؟</dt>
                        <dd>لن يتم إلغاء تأشيرة المرافق في أي حال من الأحوال وسيسمح له البقاء داخل المملكة طالما اقامته سارية.</dd>
                        <dt><i class="fa fa-envelope-o"></i> هل هناك حد أعلى لعدد المرات التي يمكن للمنشأة الاستفادة من خدمات أجير للمرافقين لتنظيم عمل مرافقين جدد أو تجديد وثائق مرافقين سبق التعاقد معهم؟</dt>
                        <dd>لا يوجد حد أعلى من أجير، ولكن يجب استيفاء متطلبات نطاقات والتي ستطبق نفس متطلبات طلب تأشيرة جديدة.</dd>
                        <dt><i class="fa fa-envelope-o"></i> ماذا لو وقع خلاف بين المرافق وصاحب العمل، هل يمكن تقديم شكوى لمكتب العمل أو الهيئات العمالية بخصوص ذلك؟</dt>
                        <dd>نعم يمكن ذلك حيث أن وزارة العمل والتنمية الاجتماعية صرحت للمرافق بالعمل المؤقت.</dd>
                        <dt><i class="fa fa-envelope-o"></i> ماذا لو المرافق فسخ العقد المبرم بيننا، هل سيتم استرجاع المبلغ المدفوع لإصدار الإشعار أو التعويض بإشعار آخر مجاناً؟
                        <dd>لن يتم التعويض في هذه الحالة.</dd>
                        <dt><i class="fa fa-envelope-o"></i> ماذا لو أن المرافق فسخ العقد المبرم بين المرافق والجهة الموظفة، هل سيتم الغاء احتسابه من نطاقات المنشأة التي تعاقدت معه؟</dt>
                        <dd>ينبغي في هذه الحالة الضغط على "الغاء الإشعار" عن طريق موقع أجير وذلك في قسم إشعارات المرافقين، علماً بأنه لن تسترد التكلفة المدفوعة.</dd>
                        <dt><i class="fa fa-envelope-o"></i> ماذا لو قُدم طلب لإصدار إشعار ولم يتم الدفع، هل من الممكن الغاء الطلب كي لا يظهر في صفحة الفواتير؟</dt>
                        <dd>نعم يمكن ذلك عن طريق موقع أجير وذلك بالضغط على "الغاء الطلب"، وذلك في قسم إشعارات المرافقين</dd>
                        <dt><i class="fa fa-envelope-o"></i> ما هو عدد المرافقين الذي يمكنني تسجيلهم عن طريق خدمة أجير؟</dt>
                        <dd>لا يوجد حد أعلى من أجير، ولكن يجب استيفاء متطلبات نطاقات والتي ستطبق نفس متطلبات طلب تأشيرة جديدة.</dd>
